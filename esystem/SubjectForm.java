    public void showRecords() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel(); 
        tableModel.setRowCount(0);

        try {
            String query = "SELECT * FROM subjects";
            ESystem.rs = ESystem.st.executeQuery(query);
            while(ESystem.rs.next()) {
                String sid = ESystem.rs.getString("ID");
                String scode = ESystem.rs.getString("Code");
                String sdesc = ESystem.rs.getString("Description");
                String sunits = ESystem.rs.getString("Units");
                String ssched = ESystem.rs.getString("Schedule");

                String[] item = {sid, scode, sdesc, sunits, ssched};
                tableModel.addRow(item);
            }
        } catch (Exception ex) { System.out.println(ex); }
    }
    
    private void showEnrollRecords(int subjectId) {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); 
        
        try {
            String query = "SELECT * FROM students JOIN Enroll ON students.ID = Enroll.studid WHERE Enroll.subjid = " + subjectId;
            
            ESystem.rs = ESystem.st.executeQuery(query);
            
            while (ESystem.rs.next()) {
                String studentId = ESystem.rs.getString("ID");
                String name = ESystem.rs.getString("Name");
                String address = ESystem.rs.getString("Address");
                String contact = ESystem.rs.getString("Contact");
                String gender = ESystem.rs.getString("Gender");
                String yearLevel = ESystem.rs.getString("YearLevel");
                
                model.addRow(new String[]{studentId, name, address, contact, gender, yearLevel});
            }
            
        } catch (Exception ex) { System.out.println(ex); }
    }


