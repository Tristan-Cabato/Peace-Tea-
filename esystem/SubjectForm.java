    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int row = jTable1.getSelectedRow();
            if (row < 0) return;
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            if (model.getRowCount() == 0) return;
            
            String subjectId = model.getValueAt(row, 0).toString();
            if (subjectId == null || subjectId.trim().isEmpty()) return;
            
            Subjid.setText(subjectId);
            Subjcode.setText(model.getValueAt(row, 1).toString());
            Subjdesc.setText(model.getValueAt(row, 2).toString());
            Subjunits.setText(model.getValueAt(row, 3).toString());
            sched.setText(model.getValueAt(row, 4).toString());
            showEnrollRecords(Integer.valueOf(subjectId));
        } catch (Exception e) { 
            System.err.println("Error in jTable1MouseClicked: " + e.getMessage());
        }   
    }//GEN-LAST:event_jTable1MouseClicked

